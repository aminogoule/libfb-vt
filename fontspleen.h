/* Binary search the sorted glyph table; fall back to '?' on a miss. */
static const unsigned char* glyph_for(uint32_t cp) {
	int lo = 0, hi = SPLEEN_NGLYPHS - 1;
	while (lo <= hi) {
		int mid = (lo + hi) / 2;
		uint32_t m = spleen_glyphs[mid].cp;
		if (cp == m) return spleen_glyphs[mid].bits;
		if (cp < m) hi = mid - 1; else lo = mid + 1;
	}
	return spleen_glyphs[SPLEEN_FALLBACK].bits;
}

